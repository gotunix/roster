package models

// Host represents a single Ansible host
type Host struct {
	Name string
	Vars map[string]interface{}
}

// Group represents an Ansible group defined in its own YAML file
type Group struct {
	Name     string
	Hosts    []string               `yaml:"hosts,omitempty"`
	Vars     map[string]interface{} `yaml:"vars,omitempty"`
	Children []string               `yaml:"children,omitempty"`
}

// Inventory represents the merged view of all inventory files
type Inventory struct {
	Hosts  map[string]*Host
	Groups map[string]*Group
}
