package tui

import (
	"github.com/charmbracelet/bubbles/spinner"
	tea "github.com/charmbracelet/bubbletea"
	"github.com/charmbracelet/lipgloss"
)

// SpinnerModel is a reusable spinner with theme-colored accent.
type SpinnerModel struct {
	inner spinner.Model
	label string
}

// NewSpinner creates a new SpinnerModel with the default CatMochaMauve accent.
func NewSpinner(label string) SpinnerModel {
	s := spinner.New(
		spinner.WithStyle(lipgloss.NewStyle().Foreground(CatMochaMauve)),
	)
	return SpinnerModel{inner: s, label: label}
}

func (m SpinnerModel) Init() tea.Cmd {
	return m.inner.Tick
}

func (m SpinnerModel) Update(msg tea.Msg) (SpinnerModel, tea.Cmd) {
	var cmd tea.Cmd
	m.inner, cmd = m.inner.Update(msg)
	return m, cmd
}

func (m SpinnerModel) View() string {
	return m.inner.View() + " " + m.label
}
