package main

import (
	"fmt"
	"os"

	"github.com/spf13/cobra"
	"gotunix.net/roster/internal/ui"
)

var rootCmd = &cobra.Command{
	Use:   "roster",
	Short: "Ansible inventory manager",
	Long:  `A high-performance CLI engine for managing Ansible inventories, groups, and variables using standard YAML formats.`,
	Run: func(cmd *cobra.Command, args []string) {
		cmd.Help()
	},
}

func Execute() {
	if err := rootCmd.Execute(); err != nil {
		fmt.Println(err)
		os.Exit(1)
	}
}

var inventoryPaths []string

func init() {
	rootCmd.SetHelpFunc(ui.HandleHelp)
	rootCmd.SetUsageFunc(ui.HandleUsage)
}
